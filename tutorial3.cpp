// Basic I/O in c++
#include<iostream>
using namespace std;
int main(){
    int num1, num2;
    cout<<"Enter the value of num1: \n"; //this is called the insertion operator
    cin>>num1;
    cout<<"Enter the value of num2: \n"; //this is called the insertion operator
    cin>>num2;
    cout<<"The sum is : "<<num1+num2;
    return 0;
}